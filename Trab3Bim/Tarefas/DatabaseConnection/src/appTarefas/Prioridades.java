package appTarefas;

public class Prioridades {
	private int id;
	private int NivelPri;
	private String DescricaoPri;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNivelPri() {
		return NivelPri;
	}
	public void setNivelPri(int nivelPri) {
		this.NivelPri = nivelPri;
	}
	public String getDescricaoPri() {
		return DescricaoPri;
	}
	public void setDescricaoPri(String descricaoPri) {
		DescricaoPri = descricaoPri;
	}
}
